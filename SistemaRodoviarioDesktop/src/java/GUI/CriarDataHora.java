    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        InitialContext context = new InitialContext();
        DataHoraSessionBeanRemote dhBean = (DataHoraSessionBeanRemote) context.lookup("ejb/DataHora");
        LinhaSessionBeanRemote linhaBean = (LinhaSessionBeanRemote) context.lookup("ejb/Linha");
        String s = linhasList.getSelectedValue().toString();
        String cidadeOrigem = s.substring(6, s.indexOf("-")-1);
        String cidadeDestino = s.substring(s.indexOf("-")+2);
        Linha linha = linhaBean.retornaLinhaToda(cidadeOrigem, cidadeDestino);
        DataHora dataHora = new DataHora(dataText.getText(), horaSaida.getText(), horaChegada.getText(), linha);
        dhBean.salvar(dataHora);
        dataHora = dhBean.retornaUltimoSalvo();
        OnibusSessionBeanRemote onibusBean = (OnibusSessionBeanRemote)context.lookup("ejb/Onibus");
        Onibus onibus = new Onibus(linha, dataHora);
        onibusBean.salvar(onibus);
        onibus = onibusBean.retornaUltimoSalvo();
        for (int i = 1; i<=20; i++) {                      
            Assento assento = new Assento ((short)1, String.valueOf(i), onibus);
            onibusBean.salvarAssento(assento);
        }
        JOptionPane.showMessageDialog(null, "Data e Hora ("+dataText.getText()+" ,"+horaSaida.getText()+") criados para a linha "+cidadeOrigem+" - "+cidadeDestino+"!");
        new Gerenciar().setVisible(true);
        this.dispose();
        } catch (Exception e) {}
        
    }//GEN-LAST:event_jButton1ActionPerformed

